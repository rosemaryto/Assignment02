var langPref = window.prompt("What language do you prefer? es, de, en, fr");

if (langPref === "es") {
    window.console.log("Hola mundo!");
} else if (langPref === "de") {
    window.console.log("Hallo welt!");
} else if (langPref === "en") {
    window.console.log("Hello world!");
} else if (langPref === "fr") {
    window.console.log("Bonjour le monde!");
} else {
    window.console.log("Hello world!");
}
